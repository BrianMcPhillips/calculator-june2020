export function addition(value1, value2) {
    return Number(value1) + Number(value2);
}
  
export function subtract(value1, value2) {
    return Number(value1) - Number(value2);
}
  
export function multiply(value1, value2) {
    return Number(value1) * Number(value2);
}
  
export function divide(value1, value2) {
    return Number(value1) / Number(value2);
}